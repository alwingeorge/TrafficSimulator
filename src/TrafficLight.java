import java.awt.Color;

public class TrafficLight {

    // Attributes..
    private int roadNumber; // road number..
    private Color color; // color of traffic light

    /**
     * Constructor.
     *
     * @param roadNumber
     * @param color
     */
    public TrafficLight(int roadNumber, Color color) {

        this.roadNumber = roadNumber;
        this.color = color;

    }
}